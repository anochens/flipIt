 <h2>Basic Rules</h2>
<p>You will be playing multiple rounds of a two-player game called
 FlipIt. The objective of FlipIt is to gain and maintain possession of
 the game board. The board has two colors - red one one side, and blue
 on the other, and it can be flipped by either you or the other
 player.  When it shows your color ("blue"), you have possession of
 the board. </p>

<p>However, until you flip the board, the state of possession of the game board is hidden from your view. In this state, you will not see the board on the screen.</p> 
<p>The only action you have available is to 'flip' the game board. When you "flip" the board, you will see the board, but the game will also reveal some important information.</p>

<p>If you flip at a time when you already own the board, then you will see a large blue "X" on the screen.  You will maintain possession of the board, and it won't actually change colors.</p>

<p>If you flip and regain possession of the board from the opponent, you will see a graphic on the screen that shows how long you had the board beforehand (a blue bar), and how long the opponent had it until you flipped (a red bar).  You will also see whether the opponent flipped multiple times (more than necessary): one or more small red "X" symbols will indicate this.</p>

<p>At the time of your flip, this information will only be shown to you and not to your opponent.</p>
  
<p>The same rules apply to your opponent.</p>

<p>Again, you cannot observe if and when the opponent flipped the board in the past, until you take the action to flip the board yourself.</p>
